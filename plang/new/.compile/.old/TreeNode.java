class TreeNode {
    TreeNode head = null;
    TreeNode left = null;
    TreeNode right = null;

    public TreeNode getHead() {
	return this.head;
    }
    public TreeNode getLeft() {
	return this.left;
    }
    public TreeNode getRight() {
	return this.right;
    }
    
    public void setHead(TreeNode v) {
	this.head = v;
    }
    public void setLeft(TreeNode v) {
	this.left = v;
    }
    public void setRight(TreeNode v) {
	this.right = v;
    }   
}